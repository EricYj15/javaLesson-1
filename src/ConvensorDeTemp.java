public class ConvensorDeTemp {

    public static void main(String[] args) {
        double tempc = 36;
        double tempf = tempc * 9 / 5 + 32;

        System.out.println("Sua temperatura em Celcius é: " + tempc);
        System.out.print("Sua temp em Fahrenheit é: " + tempf);
    }
}
